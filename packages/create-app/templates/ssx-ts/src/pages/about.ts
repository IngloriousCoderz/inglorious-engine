import { Api, html } from "@inglorious/web"

import type { AboutEntity } from "../../types"

export const about = {
  click(entity: AboutEntity) {
    entity.name += "!"
  },

  render(entity: AboutEntity, api: Api) {
    return html`
      <div>
        <h1>
          About
          <span @click=${() => api.notify(`#${entity.id}:click`)}
            >${entity.name}</span
          >
        </h1>
        <p>This is a static page generated by SSX.</p>
        <a href="/">Back Home</a>
      </div>
    `
  },
}

export const metadata = {
  title: "About",
}
